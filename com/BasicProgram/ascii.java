package com.BasicProgram;

public class ascii {
    // if we want to pass any char value as a integer
    // then ASCII value of char is return
    public static void main(String[] args) {
        System.out.println((int) 'A'); // print 65
        System.out.println((int) 'a'); // print 97
        System.out.println((int) 'b'); // print 98
        System.out.println((int) 'B'); // print 66
        System.out.println((int) 'C'); // print 67
        System.out.println((int) 'c'); // print 99
        System.out.println((int) 'D'); // print 68
        System.out.println((int) 'd'); // print 100
        System.out.println((int) 'E'); // print 69
        System.out.println((int) 'F'); // print 70
        System.out.println((int) 'G'); // print 71
        System.out.println((int) 'H'); // print 72
        System.out.println((int) 'I'); // print 73
    }

}
